   void drawAnimation(int x1, int y1, int z1, int arreglo[], int x2, int y2, int z2)
   {
      int i=0;
      double a;
      a = 90*seconds();
      glPushMatrix();
            glRotatef(a,0,1,0);
         for(int y3=y1; y3<y2;y3++){
            for(int z3=z1; z3<z2; z3++){
               for(int x3=x1; x3<x2; x3++){
                  drawCube(x3,y3,z3, arreglo[i]);
                  i++;
               }
            }
         }
      glPopMatrix();
   }
   
